use master;

GO
CREATE DATABASE SocialMediaDB;

GO
use SocialMediaDB;


GO 
CREATE TABLE Users
(
[Id] INT PRIMARY KEY IDENTITY,
[Name] NVARCHAR(100) NOT NULL
);

GO 
CREATE TABLE Posts
(
[Id] INT PRIMARY KEY IDENTITY,
[Title] NVARCHAR(100) NOT NULL,
[UserId] INT NOT NULL FOREIGN KEY REFERENCES Users(Id) ON DELETE CASCADE
);





